import UIKit

/**
 Accessors for all the colors used.
 */
extension UIColor {
    
    class func scorpionColor() -> UIColor {
        
        return UIColor.colorWithHex("606060")!
    }
    
    class func altoColor() -> UIColor {
        
        return UIColor.colorWithHex("DFDFDF")!
    }
}